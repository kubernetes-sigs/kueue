# Generates each e2e environment's controller_manager_config.yaml from the shared
# fragments and local deltas declared in that environment's generate.yaml, so that
# fields shared across environments are defined once instead of hand-copied.
#
# See test/e2e/config/components/manager-config/ for the shared fragments and
# docs/... for the fragment/generate/verify workflow.

set -o errexit
set -o nounset
set -o pipefail

YQ=${YQ:-./bin/yq}

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
CONFIG_DIR="${ROOT_DIR}/test/e2e/config"

HEADER="# Code generated by hack/generate-e2e-manager-configs.sh. DO NOT EDIT."

# Field order matches apis/config/v1beta2's Configuration struct order; fixing it
# here makes generated output deterministic regardless of fragment merge order.
FIELD_ORDER='["apiVersion","kind","metrics","leaderElection","controller","fairSharing","waitForPodsReady","internalCertManagement","clientConnection","integrations","featureGates","tls","resources"]'

# yq's default sequence style indents list items one level under their key
# (e.g. "cipherSuites:\n    - foo"). Every list in these configs uses the
# compact/indentless style instead ("frameworks:\n  - foo"), except the
# tls.cipherSuites block, which keeps the indented style it has always had.
# Generate with the compact style globally, then re-indent just that one block.
reindent_cipher_suites() {
  awk '
    /^  cipherSuites:$/ { print; in_block=1; next }
    in_block && /^  - / { sub(/^  /, "    "); print; next }
    { in_block=0; print }
  '
}

generate_one() {
  local manifest="$1"
  local dir
  dir="$(dirname "${manifest}")"
  local output
  output="$(${YQ} eval '.output' "${manifest}")"

  local fragments=()
  while IFS= read -r frag; do
    fragments+=("${dir}/${frag}")
  done < <(${YQ} eval '.fragments[]' "${manifest}")

  {
    echo "${HEADER}"
    # shellcheck disable=SC2016 # $item is a yq variable, not a shell one; must stay unexpanded.
    "${YQ}" --yaml-compact-seq-indent eval-all \
      '. as $item ireduce ({}; . * $item) | pick('"${FIELD_ORDER}"')' \
      "${fragments[@]}" \
      | reindent_cipher_suites
  } > "${dir}/${output}"
}

while IFS= read -r manifest; do
  generate_one "${manifest}"
done < <(find "${CONFIG_DIR}" -name generate.yaml | sort)
