// Package v1beta1 contains API Schema definitions for the kueue v1beta1 API group
// +kubebuilder:object:generate=true
// +groupName=kueue.x-k8s.io
package v1beta1

import (
	metav1 "k8s.io/apimachinery/pkg/apis/meta/v1"
	"k8s.io/apimachinery/pkg/runtime"
	"k8s.io/apimachinery/pkg/runtime/schema"

	"sigs.k8s.io/kueue/apis/kueue/v1beta2"
)

var (
	// SchemeGroupVersion is a group version used to register these objects.
	SchemeGroupVersion = schema.GroupVersion{Group: "kueue.x-k8s.io", Version: "v1beta1"}

	// SchemeBuilder is used to add go types to the GroupVersionKind scheme
	SchemeBuilder = runtime.NewSchemeBuilder(addKnownTypes)

	// AddToScheme adds the types in this group-version to the given scheme.
	AddToScheme = SchemeBuilder.AddToScheme

	// localSchemeBuilder is used to register autogenerated conversion and defaults functions
	// It is required by ./zz_generated.conversion.go and ./zz_generated.defaults.go
	localSchemeBuilder = &SchemeBuilder
)

func addKnownTypes(scheme *runtime.Scheme) error {
	scheme.AddKnownTypes(SchemeGroupVersion,
		&AdmissionCheck{}, &AdmissionCheckList{},
		&ClusterQueue{}, &ClusterQueueList{},
		&Cohort{}, &CohortList{},
		&LocalQueue{}, &LocalQueueList{},
		&MultiKueueConfig{}, &MultiKueueConfigList{}, &MultiKueueCluster{}, &MultiKueueClusterList{},
		&ProvisioningRequestConfig{}, &ProvisioningRequestConfigList{},
		&ResourceFlavor{}, &ResourceFlavorList{},
		&Topology{}, &TopologyList{},
		&Workload{}, &WorkloadList{},
		&WorkloadPriorityClass{}, &WorkloadPriorityClassList{},
	)
	metav1.AddToGroupVersion(scheme, SchemeGroupVersion)
	return nil
}

// Resource is required by pkg/client/listers/...
func Resource(resource string) schema.GroupResource {
	return SchemeGroupVersion.WithResource(resource).GroupResource()
}

var convScheme = runtime.NewScheme()

func init() {
	// register generated conversions into our local scheme
	_ = AddToScheme(convScheme)
	_ = v1beta2.AddToScheme(convScheme)
}
